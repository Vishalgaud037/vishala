package samplearray;

import java.util.Arrays;

public class SampleArraysCreation {
	public static void main(String[] args) {
		String[] colors = new String[5];
		System.out.println(colors);
		System.out.println(Arrays.toString(colors));
		colors [0] = "Red";
		colors [1] = "Black";
		colors [2] = "White";
		colors [3] = "Blue";
		colors [4] = "Green";
		System.out.println(Arrays.toString(colors));
		
		
		
	}
	
}
	